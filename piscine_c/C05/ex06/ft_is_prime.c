int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 2)
		return (0);
	while (i * i <= nb && i < 47000)
	{
		if (nb % i == 0)
			return (0);
		if (i % 2 == 0)
			i++;
		else
			i += 2;
	}
	return (1);
}
