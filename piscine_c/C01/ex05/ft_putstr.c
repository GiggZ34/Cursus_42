#include <unistd.h>

void	rprint(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		rprint(str[i]);
		i++;
	}
}
