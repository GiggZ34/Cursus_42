#include <unistd.h>

void	ft_print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_print('-');
		if (nb == -2147483648)
		{
			nb += 2000000000;
			ft_print('2');
		}
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_print(nb % 10 + 48);
}
