int	ft_str_is_alpha(char *str)
{
	int alpha;
	int i;

	i = 0;
	alpha = 1;
	while (str[i] != 0)
	{
		if (str[i] < 64 || (str[i] > 91 && str[i] < 97) || str[i] > 122)
			alpha = 0;
		i++;
	}
	return (alpha);
}
