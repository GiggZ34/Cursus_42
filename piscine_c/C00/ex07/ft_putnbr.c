#include <unistd.h>

void	rprint(char x)
{
	write(1, &x, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "2", 1);
			nb += 2000000000;
		}
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	rprint(48 + nb % 10);
}
